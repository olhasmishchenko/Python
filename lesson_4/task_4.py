#По даному натуральному `n ≤ 9` виведіть драбинку з `n`
# сходинок, `i`-я сходинка складається з чисел від 1 до `i`
# без прогалин.

num = int(input('Введiть натуральне число: '))
if num <= 9:
    for i in range(1, num + 1):
        print(''.join(str(n) for n in range(1, i + 1)))
else:
    print(f'{num} не натуральне число')
